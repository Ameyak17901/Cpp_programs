#include<iostream>
using namespace std;
int main()
{
	int a,b,sum,sub,mul,div,mod;
	cout<<"Enter two numers : "<<endl;
	cin>>a>>b;
	sum=a+b;
	sub=a-b;
	mul=a*b;
	div=a/b;
	mod=a%b;
	cout<<"Sum : "<<sum<<endl;
	cout<<"Subraction : "<<sub<<endl;
	cout<<"Multiplication : "<<mul<<endl;
	cout<<"Division : "<<div<<endl;
	cout<<"Modulo : "<<mod<<endl;
	
}
